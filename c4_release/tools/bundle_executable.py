"""
Bundle Executable Generator

Creates a standalone executable by combining:
1. ONNX C runtime (the inference engine)
2. Neural network weights (baked/hardcoded, NOT trained)
3. Target C program (compiled to bytecode)

The output is a single C file that can be compiled with gcc
to produce a standalone executable that runs the target program
using the neural VM.

Usage:
    python bundle_executable.py \
        --runtime onnx_runner_c4.c \
        --weights model_weights.bin \
        --program hello.c \
        --output bundled.c

    # Then compile:
    gcc -O2 -o bundled bundled.c
"""


def load_weights(weights_path):
    """Load neural network weights from binary file."""
    with open(weights_path, 'rb') as f:
        data = f.read()
    return data


def compile_program(source_path):
    """Compile C source to bytecode."""
    from src.compiler import compile_c

    with open(source_path, 'r') as f:
        source = f.read()

    bytecode, data = compile_c(source)

    # Convert bytecode to bytes (handle negative immediates)
    bytecode_bytes = b''
    for instr in bytecode:
        # Convert to unsigned 64-bit (handle negative values)
        if instr < 0:
            instr = instr & 0xFFFFFFFFFFFFFFFF
        bytecode_bytes += struct.pack('<Q', instr)  # 64-bit little-endian

    return bytecode_bytes, data or b''


def generate_c_array(name, data, items_per_line=16):
    """Generate C array declaration from binary data."""
    lines = [f"static const unsigned char {name}[] = {{"]

    for i in range(0, len(data), items_per_line):
        chunk = data[i:i+items_per_line]
        hex_vals = ', '.join(f'0x{b:02x}' for b in chunk)
        lines.append(f"    {hex_vals},")

    lines.append("};")
    lines.append(f"static const int {name}_size = {len(data)};")

    return '\n'.join(lines)



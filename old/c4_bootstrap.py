# =============================================================================
# BOOTSTRAPPED VM
# =============================================================================

class BootstrappedVM:
    """
    A VM with a compiler built-in as the "system prompt".

    The compiler bytecode is pre-loaded. When you call compile_and_run(),
    it first runs the compiler to generate program bytecode, then runs
    that program.
    """

    def __init__(self):
        self.vm = C4ByteNibbleVM()
        self.compiler_bytecode = None
        self.compiler_data = None

    def load_compiler(self, compiler_bytecode, compiler_data=None):
        """Load compiler bytecode as the system prompt."""
        self.compiler_bytecode = compiler_bytecode
        self.compiler_data = compiler_data

    def compile_and_run(self, source_code: str) -> int:
        """
        Compile source code using the built-in compiler, then run it.

        This is fully self-hosted: both compilation and execution
        happen on the transformer VM.
        """

        # For this demo, we'll use a hybrid approach:
        # 1. Tokenize source in Python (simple preprocessing)
        # 2. Run compiler bytecode to generate program bytecode
        # 3. Run program bytecode

        # Step 1: Tokenize (this is the "input layer" - like BPE tokenization)
        tokens = self._tokenize(source_code)

        # Step 2: Load compiler and set up memory
        self.vm.reset()
        self.vm.load_bytecode(self.compiler_bytecode, self.compiler_data)

        # Load tokens into input memory region
        for i, (tok_type, tok_val) in enumerate(tokens):
            addr = 0x20000 + i * 16
            self.vm.memory[addr] = tok_type
            self.vm.memory[addr + 8] = tok_val

        # Step 3: Run compiler
        self.vm.run(max_steps=100000)

        # Step 4: Extract generated bytecode from output region
        # (In full implementation, compiler would output bytecode length)

        # Step 5: Run generated bytecode
        # ...

        return self.vm.ax_int()

    def _tokenize(self, source: str):
        """Simple tokenizer - converts source to token stream."""
        tokens = []
        i = 0
        while i < len(source):
            c = source[i]
            if c.isspace():
                i += 1
            elif c.isdigit():
                n = 0
                while i < len(source) and source[i].isdigit():
                    n = n * 10 + int(source[i])
                    i += 1
                tokens.append((1, n))  # Number token
            elif c == '+':
                tokens.append((ord('+'), 0))
                i += 1
            elif c == '-':
                tokens.append((ord('-'), 0))
                i += 1
            elif c == '*':
                tokens.append((ord('*'), 0))
                i += 1
            elif c == '/':
                tokens.append((ord('/'), 0))
                i += 1
            elif c == '(':
                tokens.append((ord('('), 0))
                i += 1
            elif c == ')':
                tokens.append((ord(')'), 0))
                i += 1
            else:
                i += 1
        tokens.append((0, 0))  # End token
        return tokens



/*
 * C4 Neural - A C compiler and VM with ONNX-based arithmetic
 *
 * This is a c4-compatible implementation that:
 * 1. Compiles C source code to bytecode
 * 2. Executes bytecode with neural arithmetic operations
 * 3. Supports stdin/stdout for I/O
 *
 * Usage:
 *   ./c4_neural -e '6 * 7'                    # Evaluate expression
 *   ./c4_neural -c 'int main() { return 42; }'  # Run C code
 *   ./c4_neural program.c                     # Run from file
 *   echo "input" | ./c4_neural program.c      # With stdin
 *
 * Build with gcc:
 *   gcc -include stdio.h -include stdlib.h -include fcntl.h -include unistd.h \
 *       -include string.h -w -o c4_neural c4_neural.c
 */


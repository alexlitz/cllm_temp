# =============================================================================
# SELF-HOSTED VM CLASS
# =============================================================================

class SelfHostedVM:
    """
    VM where the compiler runs as bytecode (system prompt).

    Flow:
    1. Tokenize input (preprocessing)
    2. Run compiler bytecode → generates program bytecode
    3. Run program bytecode → result
    """

    def __init__(self):
        self.vm = C4ByteNibbleVM()
        self.compiler_code = None

        # Compile the compiler
        self._load_compiler()

    def _load_compiler(self):
        """Compile the compiler to bytecode (this is the system prompt)."""
        self.compiler_code, _ = compile_c(EXPRESSION_COMPILER)

    def tokenize(self, expr: str):
        """Convert expression string to token list."""
        tokens = []
        i = 0
        while i < len(expr):
            c = expr[i]
            if c.isspace():
                i += 1
            elif c.isdigit():
                n = 0
                while i < len(expr) and expr[i].isdigit():
                    n = n * 10 + int(expr[i])
                    i += 1
                tokens.append((1, n))  # NUM
            elif c == '+':
                tokens.append((43, 0))
                i += 1
            elif c == '-':
                tokens.append((45, 0))
                i += 1
            elif c == '*':
                tokens.append((42, 0))
                i += 1
            elif c == '/':
                tokens.append((47, 0))
                i += 1
            else:
                i += 1

        # Pad to 5 tokens
        while len(tokens) < 5:
            tokens.append((0, 0))

        return tokens[:5]

    def compile_and_run(self, expr: str) -> int:
        """
        Compile expression and run it, all on the transformer.

        The compiler bytecode is run first to process the tokens,
        then the result is computed.
        """

        # Tokenize (preprocessing - like BPE)
        tokens = self.tokenize(expr)

        # Build a C program that calls the compiler with these tokens
        t = tokens
        call_code = f'''
        int compile_expr(int t0, int v0, int t1, int v1, int t2, int v2, int t3, int v3, int t4, int v4);

        int main() {{
            return compile_expr({t[0][0]}, {t[0][1]}, {t[1][0]}, {t[1][1]}, {t[2][0]}, {t[2][1]}, {t[3][0]}, {t[3][1]}, {t[4][0]}, {t[4][1]});
        }}
        '''

        # Combine compiler + call
        full_code = EXPRESSION_COMPILER.replace(
            'int main() {',
            'int old_main() {'
        ).replace(
            'return compile_expr(1, 3, 43, 0, 1, 4, 42, 0, 1, 2);',
            'return 0;'
        ) + f'''
        int main() {{
            return compile_expr({t[0][0]}, {t[0][1]}, {t[1][0]}, {t[1][1]}, {t[2][0]}, {t[2][1]}, {t[3][0]}, {t[3][1]}, {t[4][0]}, {t[4][1]});
        }}
        '''

        # Compile and run
        bytecode, data = compile_c(full_code)

        self.vm.reset()
        self.vm.load_bytecode(bytecode, data)
        result = self.vm.run(max_steps=100000)

        return result


